package com.javaapplication;

public class arithoperator {

	public static void main(String[] args) {
		int a=2;
		int b=6;
		int sum= a+b;
		int sub= a-b;
		int mul= a*b;
		int div= a/b;
		int mod= a%b;
		
		
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(mul);
		System.out.println(div);
		System.out.println(mod);
		

	}

}
